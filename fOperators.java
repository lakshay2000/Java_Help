public class fOperators{
    public static void main(String[] args) {
        // Operators 
        int a=1;
        int b=12;
        int sum = a+b;
        System.out.println(sum);

        double c=1;
        double d=12;
        double div = c/d;
        System.out.println(div);

        // Unary Operators

        int num=1;
        num++;
        System.out.println(num);


    }
}